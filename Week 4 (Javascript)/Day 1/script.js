// Data Types

// String - Text enclosed between quotes '' ""
let string = "I am a string";
console.log(string);

// Numbers - Integers or floating-point number (decimal e.g. 1.5)
let num = 100;
console.log(num);

// Numbers enclosed in quotes are still considered strings
console.log("1000");

// Boolean - true or false
let boolean = true;
console.log(boolean);
boolean = false;
console.log(boolean);

// Null - an asigned value that means nothing
let empty = null;
console.log(empty);

// Undefined - Has been declared but not defined
console.log(undefined);

